package gov.va.ea.web.model;

public class Capability {
    private String businessFunctionId;
    private String hierarchyNumber;
    private String businessFunctionName;

    public String getBusinessFunctionId() {
	return businessFunctionId;
    }

    public void setBusinessFunctionId(String businessFunctionId) {
	this.businessFunctionId = businessFunctionId;
    }

    public String getHierarchyNumber() {
	return hierarchyNumber;
    }

    public void setHierarchyNumber(String hierarchyNumber) {
	this.hierarchyNumber = hierarchyNumber;
    }

    public String getBusinessFunctionName() {
	return businessFunctionName;
    }

    public void setBusinessFunctionName(String businessFunctionName) {
	this.businessFunctionName = businessFunctionName;
    }

}
